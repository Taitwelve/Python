for x in range(10):
    print(x)

#a variável contadora deverá assumir valores no intervalo entre 1 e 14
for x in range(1,15):
    print(x)    

#a variável contadora deverá assumir valores no intervalo entre 1 e 9 com incremento 2
for x in range(1,10,2):
    print(x)    

